//This lesson taught me the pop() function, which removes the final value from an array and returns that value so that it can be assigned to a variable or used in an operation.

// Example
var ourArray = [1,2,3];
var removedFromOurArray = ourArray.pop();
// removedFromOurArray now equals 3, and ourArray now equals [1,2]

